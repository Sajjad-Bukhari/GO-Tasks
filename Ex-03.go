package main

import "fmt"

func main() {
	name := "Mittens"
	weight := 12

	// Use %s to mean string.
	// ... Use an explicit newline.
	fmt.Printf("The cat is named %s.\n", name)

	// Use %d to mean integer.
	fmt.Printf("Its weight is %d.\n", weight)
}